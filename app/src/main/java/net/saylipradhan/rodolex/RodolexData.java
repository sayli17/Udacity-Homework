public class RodolexData {
    private String lastName, firstName, email, company, startDate, bio, avatar;

    public RodolexData(String lastName, String firstName, String email, String company, String startDate, String bio, String avatar){
        this.lastName = lastName;
        this.firstName = firstName;
        this.email = email;
        this.company = company;
        this.startDate = startDate;
        this.bio = bio;
        this.avatar = avatar;
    }

}
